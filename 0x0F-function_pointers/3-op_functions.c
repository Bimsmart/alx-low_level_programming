#include "3-calc.h"
#include <stdio.h>
/**
 * opp_add - this function adds two numbers
 * @a: first number
 * @b: second number
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * opp_sub - this function adds two numbers
 * @a: first number
 * @b: second number
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * opp_mul - this function adds two numbers
 * @a: first number
 * @b: second number
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * opp_div - this function adds two numbers
 * @a: first number
 * @b: second number
 * Return: a / b
 */
int op_div(int a,int b)
{
	return (a / b);
}
/**
 * opp_mod - this function adds two numbers
 * @a: first number
 * @b: second number
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
